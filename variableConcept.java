package com.javaTutorials;

public class variableConcept {

    public static void main(String[] args) {
        // normal comment
        // byte 1 [-128 to 127]
        // short - 2
        // int - 4, 1, 2, 3
        // long - 8
        // float - 4 3.14
        // double - 8
        // char - 2  a, b, c, d
        // boolean - 1 true / false

        byte age = 20;
        int phone = 1234567891;
        long phoneTwo = 12345678912L;
        float pi = 3.14F;
        char letter = '@';
        boolean isAdult = false;

        // Non-primitive types
        String name = "aman";




    }
}
